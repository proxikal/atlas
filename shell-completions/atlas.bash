_atlas() {
    local i cur prev opts cmd
    COMPREPLY=()
    if [[ "${BASH_VERSINFO[0]}" -ge 4 ]]; then
        cur="$2"
    else
        cur="${COMP_WORDS[COMP_CWORD]}"
    fi
    prev="$3"
    cmd=""
    opts=""

    for i in "${COMP_WORDS[@]:0:COMP_CWORD}"
    do
        case "${cmd},${i}" in
            ",$1")
                cmd="atlas"
                ;;
            atlas,ast)
                cmd="atlas__ast"
                ;;
            atlas,b)
                cmd="atlas__build"
                ;;
            atlas,build)
                cmd="atlas__build"
                ;;
            atlas,c)
                cmd="atlas__check"
                ;;
            atlas,check)
                cmd="atlas__check"
                ;;
            atlas,completions)
                cmd="atlas__completions"
                ;;
            atlas,d)
                cmd="atlas__debug"
                ;;
            atlas,debug)
                cmd="atlas__debug"
                ;;
            atlas,f)
                cmd="atlas__fmt"
                ;;
            atlas,fmt)
                cmd="atlas__fmt"
                ;;
            atlas,help)
                cmd="atlas__help"
                ;;
            atlas,lsp)
                cmd="atlas__lsp"
                ;;
            atlas,profile)
                cmd="atlas__profile"
                ;;
