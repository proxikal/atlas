                ;;
            atlas,test)
                cmd="atlas__test"
                ;;
            atlas,typecheck)
                cmd="atlas__typecheck"
                ;;
            atlas__help,ast)
                cmd="atlas__help__ast"
                ;;
            atlas__help,build)
                cmd="atlas__help__build"
                ;;
            atlas__help,check)
                cmd="atlas__help__check"
                ;;
            atlas__help,completions)
                cmd="atlas__help__completions"
                ;;
            atlas__help,debug)
                cmd="atlas__help__debug"
                ;;
            atlas__help,fmt)
                cmd="atlas__help__fmt"
                ;;
            atlas__help,help)
                cmd="atlas__help__help"
                ;;
            atlas__help,lsp)
                cmd="atlas__help__lsp"
                ;;
            atlas__help,profile)
                cmd="atlas__help__profile"
                ;;
            atlas__help,repl)
                cmd="atlas__help__repl"
                ;;
            atlas__help,run)
                cmd="atlas__help__run"
                ;;
            atlas__help,test)
                cmd="atlas__help__test"
                ;;
            atlas__help,typecheck)
                cmd="atlas__help__typecheck"
                ;;
            *)
                ;;
        esac
    done

    case "${cmd}" in
        atlas)
            opts="-h -V --help --version run r check c build b repl ast typecheck fmt f profile test t debug d lsp completions help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 1 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        atlas__ast)
            opts="-h -V --help --version <FILE>"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        atlas__build)
            opts="-p -v -q -h -V --profile --release --clean --verbose --quiet --json --help --version"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                --profile)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                -p)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        atlas__check)
            opts="-h -V --json --help --version <FILE>"
